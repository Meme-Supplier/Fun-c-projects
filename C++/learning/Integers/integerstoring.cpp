#include <iostream>

int main() {

    //integer

    int age = 21;
    int year = 2023;
    int days = 7.5;

    std::cout << age << '\n';
    std::cout << year << '\n';
    std::cout << days;

    //Double (Number including decimal)

    double price = 10.99;

    std::cout << price;
    

    //Single character

    char grade = 'A';
    char initial = 'B';

    std::cout << initial;

    return 0;
}