package expressions;

public class expressions {

	public static void main(String[] args) {
		
		// +, -, *, /, %
		
		int friends = 10;
		
		// Adds 1 friend
		friends = friends + 1;
		System.out.println(friends);
		
		int amigos = 10;
		//Multiplies amigos by 2
		amigos = amigos * 2;
		System.out.println(amigos);
		
		int enemies = 10;
		// Removes 1 enemy
		enemies = enemies - 5;
		System.out.println(enemies);
		
		int cake = 20;
		//splits cake into 2
		cake = cake / 2;
		System.out.println(cake);
		
		int cookie = 50;
		// amount of cookies left
		cookie = cookie % 9;
		System.out.println(cookie);
		
		int x = 0;
		// adds 1
		x++;
		System.out.println(x);
		
		int y = 1;
		// Subtracts 1
		y--;
		System.out.println(y);

	}

}
