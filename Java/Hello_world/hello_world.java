
public class hello_world {

	public static void main(String[] args) {
		
		// This is a comment
		
		/*
		 This
		 is
		 a
		 multi
		 line
		 comment
		 */
		
		System.out.print("Hello World!\n\n");
		
		// Prints the line to the next line
		System.out.print("I skipped a line!");
		
		// Tabs
		System.out.println("\n\tI'm tabbed!");
		
		//Quotes
		System.out.println("\"I am quoted!\"");
		
		//Backslash
		System.out.println("\\ <--Backslash");
		
		// Join multiple statements
		String x = "Statement 1";
		String y = "Statement 2";
		System.out.println(x +" and " + y);
		

	}

}
